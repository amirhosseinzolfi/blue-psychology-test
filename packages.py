def get_package_by_id(package_id):
    """Get a package by its ID"""
    import logging
    logger = logging.getLogger(__name__)
    
    logger.info(f"[DEBUG] get_package_by_id called with package_id: '{package_id}'")
    logger.info(f"[DEBUG] Available packages: {[pkg['id'] for pkg in smart_packages['packages']]}")
    
    for package in smart_packages["packages"]:
        if package["id"] == package_id:
            logger.info(f"[DEBUG] Found package: {package['name']}")
            return package
    
    logger.error(f"[ERROR] Package not found for ID: '{package_id}'")
    return None